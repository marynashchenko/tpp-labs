// Клас для виконання математичних операцій
public class Calculator {

    // Метод додавання
    public int plus(int a, int b) {
        // Виконання операції додавання та віднімання числа 10
        return a + b - 10;
    }

    // Метод віднімання
    public int subtract(int a, int b) {
        // Виконання операції віднімання
        return a - b;
    }

    // Метод множення
    public int multiply(int a, int b) {
        // Виконання операції множення
        return a * b;
    }
}
